/**
 * Build-time blog manifest generator.
 *
 * Reads `content/blog/*.md`, parses frontmatter with gray-matter, renders the
 * Markdown body to HTML with `marked`, and writes
 * `src/generated/blog-manifest.ts` plus per-post HTML snippets the prerender
 * script can inline.
 *
 * Runs in `predev`/`prebuild` so both Vite dev and production builds have an
 * up-to-date manifest. The browser bundle never imports `marked` or
 * `gray-matter` — only the generated TS file, which is just data.
 */
import { readFileSync, writeFileSync, readdirSync, mkdirSync, existsSync } from "node:fs";
import { dirname, resolve, join } from "node:path";
import { fileURLToPath } from "node:url";
import matter from "gray-matter";
import { marked } from "marked";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const CONTENT_DIR = resolve(ROOT, "content/blog");
const OUT_DIR = resolve(ROOT, "src/generated");
const OUT_FILE = resolve(OUT_DIR, "blog-manifest.ts");

marked.setOptions({ gfm: true, breaks: false });

function slugFromFilename(filename) {
  return filename.replace(/\.md$/i, "");
}

function readableDate(iso) {
  const d = new Date(iso);
  if (Number.isNaN(d.getTime())) return iso;
  return d.toLocaleDateString("en-US", {
    year: "numeric",
    month: "long",
    day: "numeric",
    timeZone: "UTC",
  });
}

function loadPosts() {
  if (!existsSync(CONTENT_DIR)) {
    console.warn(`[blog-manifest] ${CONTENT_DIR} missing — writing empty manifest`);
    return [];
  }
  const files = readdirSync(CONTENT_DIR).filter((f) => f.endsWith(".md"));
  const posts = files.map((file) => {
    const raw = readFileSync(join(CONTENT_DIR, file), "utf8");
    const { data, content } = matter(raw);
    const slug = data.slug || slugFromFilename(file);
    const html = marked.parse(content);
    const dateIso = data.date
      ? new Date(data.date).toISOString()
      : new Date().toISOString();
    return {
      slug,
      title: String(data.title || slug),
      description: String(data.description || ""),
      excerpt: String(data.excerpt || data.description || ""),
      date: dateIso,
      dateFormatted: readableDate(dateIso),
      author: String(data.author || "BuilderSuite ML Team"),
      tags: Array.isArray(data.tags) ? data.tags.map(String) : [],
      ogImage: String(data.ogImage || "/og/home.jpg"),
      html,
    };
  });
  // newest first
  posts.sort((a, b) => (a.date < b.date ? 1 : -1));
  return posts;
}

function emit(posts) {
  mkdirSync(OUT_DIR, { recursive: true });
  const body = `// AUTO-GENERATED by scripts/generate-blog-manifest.mjs. Do not edit by hand.
// Regenerate by running predev/prebuild (or: node scripts/generate-blog-manifest.mjs).

export interface BlogPost {
  slug: string;
  title: string;
  description: string;
  excerpt: string;
  date: string;            // ISO 8601
  dateFormatted: string;   // human-readable
  author: string;
  tags: string[];
  ogImage: string;
  html: string;            // rendered markdown body
}

export const blogPosts: BlogPost[] = ${JSON.stringify(posts, null, 2)};

export const blogPostsBySlug: Record<string, BlogPost> = Object.fromEntries(
  blogPosts.map((p) => [p.slug, p]),
);
`;
  writeFileSync(OUT_FILE, body, "utf8");
  console.log(`[blog-manifest] wrote ${posts.length} post(s) -> ${OUT_FILE}`);
}

emit(loadPosts());
