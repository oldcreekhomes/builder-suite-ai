-- Fix search_path for takeoff trigger functions to address security warnings

CREATE OR REPLACE FUNCTION public.update_takeoff_projects_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public;

